    private void jPanel12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel12MouseClicked
        // TODO add your handling code here:
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/k2?user=root&password=root");
            if(user == false) {
                pre = conn.prepareStatement("DELETE FROM users WHERE id = ? AND username = ?");
                pre.setString(1, jTextField1.getText());
                pre.setString(2, jTextField2.getText());
                pre.executeUpdate();
                conn.close();
                emptyField();
                showTable();
            }
            else if(user == true){
                pre = conn.prepareStatement("DELETE FROM admin WHERE id = ? AND password = ?");
                pre.setString(1, jTextField1.getText());
                pre.setString(2, jTextField2.getText());
                pre.executeUpdate();
                conn.close();
                emptyField();
                showTable();
                this.dispose();
                new Display1().setVisible(true);
            }
            
        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jPanel12MouseClicked

